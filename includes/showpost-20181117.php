<p class="mainperfdesc">James Keelaghan is an internationally recognized singer songwriter, known for his finely crafted stories and his commanding performances. Keelaghan studied history at the University of Calgary and his passion for stories old and new informs some of his best known songs . These aren’t stuffy history lessons, but an inspired discovery of people and places in another time. And while many of Keelaghan’s most celebrated songs draw from historical accounts, his accounts of personal journeys are remarkable too–tender and unflinching. Join us for this wonderful night of music. </p>
